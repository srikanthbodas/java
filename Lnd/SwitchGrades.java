public class SwitchGrades{
public static void main(String args[]){
int Grades = 70;
switch(Grades){
case 90:
System.out.println("grade A is first class");
break;
case 80:
System.out.println("grade B is second class");
break;
case 70:
System.out.println("grade C is third class");
break;
case 60:
System.out.println("grade D is fourth class");
break;
default:
System.out.println("student is fail");
break;
}
}
}